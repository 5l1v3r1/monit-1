Monit is an open-source project consisting of non-paid volunteers who
contribute work and code to the project in their own free time. The
project consists of a core team of developers and many contributors
from around the world. This page lists all of the people who have gone
the extra mile and have contributed to the monit project. If
<u>you</u> want to contribute code or documentation to the project you
should join the <a
href="http://lists.nongnu.org/mailman/listinfo/monit-dev">developer
mailing list</a> and post your patch or code there.


<P>We ask that you please do not send us emails privately asking for
support. Instead, we have setup a <a
href="http://lists.nongnu.org/mailman/listinfo/monit-general">mailing
list</a> which contain many individuals who will help answer detailed
requests for help. The benefit of using a mailing lists over private
communication is that it is a shared resource where others can also
learn from common mistakes.
