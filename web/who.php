<h3>Getting involved</h3>

The monit project operates on a meritocracy: the more you do, the more
responsibility you will obtain. If you would like to get involved, the
first step is to join the <A HREF=
"http://mail.freesoftware.fsf.org/mailman/listinfo/monit-general">mailing
lists</A>.

<p>The next step is to use the product, report bugs, making feature
requests, et cetera. <A
HREF="http://savannah.nongnu.org/cvs/?group=monit">Download the
developer source code via anonymous CVS</A>, get familiar with the
code base and contribute code or documentation patches. To join the
monit project team you must have contributed significant code to the
project and been an active member of the mailing lists for some
time. If you have done this and want to join the project, let us
know. As a project member you will get to vote and decide on future
directions of the project. You will also get write access to the CVS
repository.


<h3>Decision Making</h3>

All Contributors are encouraged to participate in decisions, but
the decision itself is made by those that have Committer status
in the Project. I.e. write access to the CVS repository. 

<p>Each vote can be made in one of three flavors:

