<h2>Product support</h2>

If you have questions or comments about the software or documentation
please subscribe to the <a
href="http://mail.freesoftware.fsf.org/mailman/listinfo/monit-general">monit
general mailing list</a> and post your questions there. You can also
look in the <a
href="http://mail.freesoftware.fsf.org/pipermail/monit-general/">mailing
list archive</a> for answers. 

<p>Please note that you must subscribe to the mailing list before you
can send a mail to the list. Mail sent to the list from a
non-subscriber is automatically <i>deleted</i>.
   
<h2>Announcements</h2>

New version announcements are sent to the 
<a
href="http://mail.freesoftware.fsf.org/mailman/listinfo/monit-general">monit
general mailing list</a> and low-traffic 
<a
href="http://mail.freesoftware.fsf.org/mailman/listinfo/monit-announce">monit
announce mailing list</a>.

